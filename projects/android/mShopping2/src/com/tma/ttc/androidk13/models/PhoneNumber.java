package com.tma.ttc.androidk13.models;

public class PhoneNumber {
	private String type, number;
	
	public void setType(String type) {
		this.type = type;
	}

	public String getType() {
		return type;
	}

	public void setNumber(String number) {
		this.number = number;
	}

	public String getNumber() {
		return number;
	}
}
